package com.invessence.web.data.consumer.CTO;

public class Financial
{

   private Long acctnum;
   private Integer annualincome;
   private Integer networth;
   private Integer liquidnetworth;

   private Integer expense;
   private Integer annualdebt;

   private Integer dependent;

   public Long getAcctnum()
   {
      return acctnum;
   }

   public void setAcctnum(Long acctnum)
   {
      this.acctnum = acctnum;
   }

   public Integer getAnnualincome()
   {
      return annualincome;
   }

   public void setAnnualincome(Integer annualincome)
   {
      this.annualincome = annualincome;
   }

   public Integer getNetworth()
   {
      return networth;
   }

   public void setNetworth(Integer networth)
   {
      this.networth = networth;
   }

   public Integer getLiquidnetworth()
   {
      return liquidnetworth;
   }

   public void setLiquidnetworth(Integer liquidnetworth)
   {
      this.liquidnetworth = liquidnetworth;
   }

   public Integer getExpense()
   {
      return expense;
   }

   public void setExpense(Integer expense)
   {
      this.expense = expense;
   }

   public Integer getAnnualdebt()
   {
      return annualdebt;
   }

   public void setAnnualdebt(Integer annualdebt)
   {
      this.annualdebt = annualdebt;
   }

   public Integer getDependent()
   {
      return dependent;
   }

   public void setDependent(Integer dependent)
   {
      this.dependent = dependent;
   }
}
