DROP TABLE IF EXISTS `user_logon`;

CREATE TABLE `user_logon` (
  `logonid` bigint(20) NOT NULL AUTO_INCREMENT,
  `userid` varchar(60) NOT NULL,
  `pwd` varchar(60) NOT NULL,
  `logonstatus` varchar(1) NOT NULL DEFAULT 'T',
  `prefix` varchar(25) DEFAULT NULL,
  `lastname` varchar(25) DEFAULT NULL,
  `firstname` varchar(25) DEFAULT NULL,
  `middlename` varchar(25) DEFAULT NULL,
  `suffix` varchar(25) DEFAULT NULL,
  `email` varchar(60) NOT NULL,
  `emailalt` varchar(60) DEFAULT NULL,
  `stateRegistered` varchar(2) DEFAULT NULL,
  `leadSource` varchar(25) DEFAULT NULL,
  `cid` varchar(3) DEFAULT NULL,
  `advisor` varchar(20) DEFAULT NULL,
  `rep`     varchar(20) DEFAULT NULL,
  `question1` varchar(60) DEFAULT NULL,
  `answer1` varchar(60) DEFAULT NULL,
  `question2` varchar(60) DEFAULT NULL,
  `answer2` varchar(60) DEFAULT NULL,
  `question3` varchar(60) DEFAULT NULL,
  `answer3` varchar(60) DEFAULT NULL,
  `ip` varchar(20) DEFAULT NULL,
  `resetID` varchar(8) DEFAULT NULL,
  `emailmsgtype` varchar(45) DEFAULT NULL,
  `access` varchar(10) DEFAULT NULL,
  `atstart` varchar(20) DEFAULT NULL,
  `created` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `lastupdated` timestamp NULL DEFAULT NULL,
  PRIMARY KEY (`logonid`),
  UNIQUE KEY `PK2_UserID_UNIQUE` (`userid`),
  UNIQUE KEY `PK3_UserID_UNIQUE` (`email`)
) ENGINE=InnoDB AUTO_INCREMENT=1000 DEFAULT CHARSET=utf8;
