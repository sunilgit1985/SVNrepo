   public void taxRate()
   {
      if (getTotalIncome() == null)
      {
         setTaxRate(0.10);
      }
      else if (getTotalIncome() > 400000)
      {
         setTaxRate(0.396);
      }
      else if (getTotalIncome() > 398350 && getTotalIncome() <= 400000)
      {
         setTaxRate(0.35);
      }
      else if (getTotalIncome() > 183250 && getTotalIncome() <= 398350)
      {
         setTaxRate(0.33);
      }
      else if (getTotalIncome() > 87850 && getTotalIncome() <= 183250)
      {
         setTaxRate(0.28);
      }
      else if (getTotalIncome() > 36250 && getTotalIncome() <= 87850)
      {
         setTaxRate(0.25);
      }
      else if (getTotalIncome() > 8925 && getTotalIncome() <= 36250)
      {
         setTaxRate(0.15);
      }
      else if (getTotalIncome() > 0 && getTotalIncome() <= 8925)
      {
         setTaxRate(0.10);
      }
   }

